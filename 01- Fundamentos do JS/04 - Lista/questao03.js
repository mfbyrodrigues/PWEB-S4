// Conversão de temperaturas (Celsius para Fahrenheit)

console. log ("Convertor de temperatura: Celsius -> Fahrenheit");
let temperatura_celsius = prompt ("Temperatura em Celsius:"); // Usuário informa a temperatura, em Celsius 

let temperatura_fahrenheit = ((temperatura_celsius * 9/5) + 32); // Cálculo para conversão de temperatura
console.log ("Temperatura em Fahrenheit: " + temperatura_fahrenheit); // Mostra a conversão feita